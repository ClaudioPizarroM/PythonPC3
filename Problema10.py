#Crearemos un programa menú que nos permita realizar la gestión de una Biblioteca. La
#estructura del programa será la siguiente
#biblioteca/
#__init__.py
#libro.py
#gestion.py
#main.py
#Clase Libro (archivo libro.py):
#● Atributos: titulo, autor, isbn.
#● Métodos:
#○ __init__ para inicializar los atributos.
#○ __str__ para representar el libro como una cadena.
#Clase GestionBiblioteca (archivo gestion.py):
#● Atributos: libros (lista de libros).
#● Métodos:
#○ __init__ para inicializar la lista de libros vacía.
#○ agregar_libro(libro) para agregar un libro a la lista.
#○ listar_libros() para listar todos los libros.
#○ buscar_por_titulo(titulo) para buscar libros por título.
#○ buscar_por_autor(autor) para buscar libros por autor.
#Archivo main.py:
#Programa menu que permita
#1. Agrega un libro a nuestra biblioteca
#2. Listar los libros en nuestra biblioteca
#3. Buscar un libro según su título
#4. Buscar un libro según su autor.
